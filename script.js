function MostrarModal1() {
        var display = document.getElementById("modal1").style.display = "none";
        if(display == "none")
            document.getElementById("modal1").style.display = 'block';
        else
            document.getElementById("modal1").style.display = 'none';
    }

    function OcultarModal1() {
        var display = document.getElementById("modal1").style.display;
        if(display == "block")
            document.getElementById("modal1").style.display = 'none';
        else
            document.getElementById("modal1").style.display = 'block';
    }

